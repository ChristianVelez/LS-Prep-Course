arr = [15, 7, 18, 5, 12, 8, 5, 1]

1. arr.index(5)  # index method will return the index of the first value that matches
				 # the argument and in this case the first number 5 is at index 3 of the array

2. arr.index[5]  # index method with bracket notation does not work, this will return an error

3. arr[5] # this will return the value at index 5 of the array, which is the number 8
